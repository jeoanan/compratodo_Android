package com.cesde.compratodo.Entities;

public class Check {
    private String idCheck;
    private String nameCheck;
    private String descriptionCheck;
    private String categoryCheck;
    private double valueCheck;
    private int quantityCheck;
    private String shopCheck;
    private String emailCheck;

    public String getIdCheck() {
        return idCheck;
    }

    public void setIdCheck(String idCheck) {
        this.idCheck = idCheck;
    }

    public String getNameCheck() {
        return nameCheck;
    }

    public void setNameCheck(String nameCheck) {
        this.nameCheck = nameCheck;
    }

    public String getDescriptionCheck() {
        return descriptionCheck;
    }

    public void setDescriptionCheck(String descriptionCheck) {
        this.descriptionCheck = descriptionCheck;
    }

    public String getCategoryCheck() {
        return categoryCheck;
    }

    public void setCategoryCheck(String categoryCheck) {
        this.categoryCheck = categoryCheck;
    }

    public double getValueCheck() {
        return valueCheck;
    }

    public void setValueCheck(double valueCheck) {
        this.valueCheck = valueCheck;
    }

    public int getQuantityCheck() {
        return quantityCheck;
    }

    public void setQuantityCheck(int quantityCheck) {
        this.quantityCheck = quantityCheck;
    }

    public String getShopCheck() {
        return shopCheck;
    }

    public void setShopCheck(String shopCheck) {
        this.shopCheck = shopCheck;
    }

    public String getEmailCheck() {
        return emailCheck;
    }

    public void setEmailCheck(String emailCheck) {
        this.emailCheck = emailCheck;
    }
}
